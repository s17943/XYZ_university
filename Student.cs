using System;

public class Student
{
	string name, surname, course, type, id, enrolled_on, email, some_name1, some_name2; 

	public Student(string name, string surname, string course, string type, string id, string enrolled_on, string email, string some_name1, string some_name2)
	{
		this.name = name;
		this.surname = surname;
		this.course = course;
		this.type = type;
		this.id = id;
		this.enrolled_on = enrolled_on;
		this.email = email;
		this.some_name1 = some_name1;
		this.some_name2 = some_name2;

	}
}
